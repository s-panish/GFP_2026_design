def parse_mutation_token(token):
    token = str(token).strip()
    match = re.fullmatch(r"([A-Z])(\d+)([A-Z])", token)

    if match is None:
        raise ValueError(f"Bad mutation token: {token}")

    ref_aa = match.group(1)
    pos = int(match.group(2))
    alt_aa = match.group(3)

    return ref_aa, pos, alt_aa


def apply_mutations_local(parent_seq, mutations):
    parent_seq = normalize_sequence(parent_seq)
    seq = list(parent_seq)
    used_positions = set()

    for mutation in mutations:
        ref_aa, pos, alt_aa = parse_mutation_token(mutation)

        if pos < 1 or pos > len(seq):
            raise ValueError(f"{mutation}: position outside sequence length {len(seq)}")

        if pos in used_positions:
            raise ValueError(f"{mutation}: position {pos} is mutated twice")

        observed = seq[pos - 1]

        if observed != ref_aa:
            raise ValueError(
                f"{mutation}: expected {ref_aa} at position {pos}, observed {observed}"
            )

        seq[pos - 1] = alt_aa
        used_positions.add(pos)

    return "".join(seq)


def chromophore_preserved(parent_seq, query_seq):
    parent_seq = normalize_sequence(parent_seq)
    query_seq = normalize_sequence(query_seq)
    return parent_seq[64:67] == query_seq[64:67]


def approximate_net_charge(seq):
    seq = normalize_sequence(seq)
    positive = seq.count("K") + seq.count("R") + 0.1 * seq.count("H")
    negative = seq.count("D") + seq.count("E")
    return round(positive - negative, 3)


def mutation_positions(mutations):
    positions = []
    for mutation in mutations:
        _, pos, _ = parse_mutation_token(mutation)
        positions.append(pos)
    return positions


def has_duplicate_positions(mutations):
    positions = mutation_positions(mutations)
    return len(positions) != len(set(positions))


