def load_exclusion_sequences(path):
    df = pd.read_csv(path, dtype=str, keep_default_na=False)
    excluded = set()

    sequence_columns = [
        c for c in df.columns
        if str(c).strip().lower() in {
            "sequence",
            "seq",
            "aa_sequence",
            "protein_sequence",
            "amino_acid_sequence"
        }
    ]

    columns_to_scan = sequence_columns if sequence_columns else list(df.columns)

    for col in columns_to_scan:
        for value in df[col].astype(str):
            candidate = normalize_sequence(value)

            if len(candidate) >= 50 and set(candidate) <= ALLOWED_AA:
                excluded.add(candidate)

    return excluded, df


