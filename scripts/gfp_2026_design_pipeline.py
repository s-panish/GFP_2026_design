def parse_reference_file(path):
    refs = {}
    current_name = None
    current_seq = []

    with open(path, "r", encoding="utf-8-sig") as handle:
        for raw_line in handle:
            line = raw_line.strip()

            if line == "":
                continue

            if line.startswith("#"):
                continue

            if line.startswith(">"):
                if current_name is not None:
                    refs[current_name] = normalize_sequence("".join(current_seq))

                current_name = line[1:].strip()
                current_seq = []
            else:
                if current_name is not None:
                    current_seq.append(line)

        if current_name is not None:
            refs[current_name] = normalize_sequence("".join(current_seq))

    return refs


def validate_protein_sequence(name, seq):
    seq = normalize_sequence(seq)

    errors = []

    if not seq.startswith("M"):
        errors.append("does not start with M")

    if len(seq) < 220 or len(seq) > 250:
        errors.append(f"length is {len(seq)}, expected 220–250")

    invalid = sorted(set(seq) - ALLOWED_AA)
    if invalid:
        errors.append(f"invalid characters: {''.join(invalid)}")

    if errors:
        raise ValueError(f"{name}: " + "; ".join(errors))

    return seq


