def parse_mutation_string(value):
    if pd.isna(value):
        return []

    value = str(value).strip()

    if value == "":
        return []

    if value.upper() in {"WT", "WILDTYPE", "WILD_TYPE", "NONE", "NAN"}:
        return []

    tokens = re.findall(r"[A-Z]\d+[A-Z]", value)
    return tokens


def mutation_list_to_feature_dict(mutations):
    return {mutation: 1.0 for mutation in mutations}


def mutations_relative_to_reference(reference_seq, query_seq):
    reference_seq = normalize_sequence(reference_seq)
    query_seq = normalize_sequence(query_seq)

    if len(reference_seq) != len(query_seq):
        raise ValueError(
            f"Reference and query lengths differ: {len(reference_seq)} vs {len(query_seq)}"
        )

    mutations = []

    for pos, (ref_aa, query_aa) in enumerate(zip(reference_seq, query_seq), start=1):
        if ref_aa != query_aa:
            mutations.append(f"{ref_aa}{pos}{query_aa}")

    return mutations


