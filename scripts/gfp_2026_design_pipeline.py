def candidate_local_brightness_score(mutations):
    score = 0.0

    for mutation in mutations:
        evidence = get_dms_evidence_for_mutation(mutation)
        score += evidence["local_dms_score"]

    if "H148S" in mutations:
        score += 1.2
    if "N164Y" in mutations:
        score += 0.15
    if "F46L" in mutations:
        score -= 0.25

    return float(score)


def candidate_stability_proxy_score(seq, mutations):
    net_charge = approximate_net_charge(seq)
    charge_delta = sfGFP_net_charge - net_charge
    charge_score = np.exp(-((charge_delta - 6.0) ** 2) / (2 * 3.0 ** 2))

    folding_count = sum(1 for m in mutations if mutation_category[m] == "folding_prior")
    supercharge_count = sum(1 for m in mutations if mutation_category[m] == "negative_supercharging")

    stability_score = (
        1.20 * charge_score +
        0.25 * folding_count +
        0.12 * supercharge_count
    )

    return float(stability_score)


def candidate_risk_penalty(mutations):
    penalty = 0.0
    penalty += 0.08 * len(mutations)

    if len(mutations) > 7:
        penalty += 0.50

    terminal_mutations = 0

    for mutation in mutations:
        _, pos, _ = parse_mutation_token(mutation)
        if pos <= 10 or pos >= 221:
            terminal_mutations += 1

    if terminal_mutations > 1:
        penalty += 0.10
    if "F46L" in mutations:
        penalty += 0.15

    return float(penalty)


def candidate_total_prescore(seq, mutations):
    brightness = candidate_local_brightness_score(mutations)
    stability = candidate_stability_proxy_score(seq, mutations)
    penalty = candidate_risk_penalty(mutations)

    total = (
        0.50 * brightness +
        0.40 * stability -
        penalty
    )

    return float(total)


