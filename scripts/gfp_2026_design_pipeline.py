def get_dms_evidence_for_mutation(sf_mutation):
    dms_key = DMS_EQUIVALENT_MUTATION.get(sf_mutation, sf_mutation)
    row = single_mut_map.get(dms_key, None)

    if row is None:
        return {
            "dms_key": dms_key,
            "has_single_observation": False,
            "observed_single_brightness_mean": np.nan,
            "ridge_coefficient": np.nan,
            "local_dms_score": 0.0
        }

    observed = row.get("observed_single_brightness_mean", np.nan)
    coef = row.get("ridge_coefficient", np.nan)

    score = 0.0

    if pd.notna(observed):
        # Нас интересует относительный сигнал около avGFP WT.
        # Слишком большие значения ограничиваем, чтобы одна мутация не доминировала.
        score += max(-0.5, min(0.8, (float(observed) - float(pred_avGFP_WT)) * 2.0))

    if pd.notna(coef):
        score += max(-0.4, min(0.4, float(coef)))

    return {
        "dms_key": dms_key,
        "has_single_observation": pd.notna(observed),
        "observed_single_brightness_mean": float(observed) if pd.notna(observed) else np.nan,
        "ridge_coefficient": float(coef) if pd.notna(coef) else np.nan,
        "local_dms_score": float(score)
    }

