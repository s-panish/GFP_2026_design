def predict_brightness_from_sequence(seq, reference_seq=avGFP):
    seq = normalize_sequence(seq)
    reference_seq = normalize_sequence(reference_seq)

    if len(seq) != len(reference_seq):
        raise ValueError(
            f"Sequence length differs from reference: {len(seq)} vs {len(reference_seq)}"
        )

    mutations = mutations_relative_to_reference(reference_seq, seq)
    feature_dict = mutation_list_to_feature_dict(mutations)

    X = vectorizer.transform([feature_dict])
    raw_pred = ridge_model.predict(X)
    calibrated_pred = isotonic_model.predict(raw_pred)

    return float(calibrated_pred[0])


