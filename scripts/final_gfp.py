def minmax(series):
    series = pd.to_numeric(series, errors="coerce")
    low = series.min()
    high = series.max()

    if pd.isna(low) or pd.isna(high) or high == low:
        return pd.Series(np.ones(len(series)) * 0.5, index=series.index)

    return (series - low) / (high - low)


