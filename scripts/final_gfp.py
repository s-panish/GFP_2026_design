def normalize_sequence(seq):
    seq = str(seq)
    seq = re.sub(r"\s+", "", seq)
    seq = seq.upper()
    return seq


def validate_submission_sequence(seq_id, sequence):
    sequence = normalize_sequence(sequence)
    errors = []

    if not sequence.startswith("M"):
        errors.append("does not start with M")

    if len(sequence) < 220 or len(sequence) > 250:
        errors.append(f"length is {len(sequence)}, expected 220-250 aa")

    invalid = sorted(set(sequence) - ALLOWED_AA)
    if invalid:
        errors.append("invalid characters: " + "".join(invalid))

    if errors:
        raise ValueError(f"Seq_ID {seq_id}: " + "; ".join(errors))

    return sequence


def recursively_extract_zip(zip_path, extract_to):
    os.makedirs(extract_to, exist_ok=True)

    with zipfile.ZipFile(zip_path, "r") as zip_ref:
        zip_ref.extractall(extract_to)

    nested_zip_files = glob.glob(os.path.join(extract_to, "**", "*.zip"), recursive=True)

    for nested_zip in nested_zip_files:
        nested_name = os.path.splitext(os.path.basename(nested_zip))[0]
        nested_dir = os.path.join(os.path.dirname(nested_zip), nested_name + "_extracted")
        os.makedirs(nested_dir, exist_ok=True)

        try:
            with zipfile.ZipFile(nested_zip, "r") as nested_ref:
                nested_ref.extractall(nested_dir)
        except zipfile.BadZipFile:
            pass


def extract_query_id_from_filename(path):
    name = os.path.basename(path)

    name = re.sub(r"_scores_rank_001.*$", "", name)
    name = re.sub(r"_unrelaxed_rank_001.*$", "", name)
    name = re.sub(r"_relaxed_rank_001.*$", "", name)
    name = re.sub(r"_predicted_aligned_error.*$", "", name)
    name = re.sub(r"_pae.*$", "", name)
    name = re.sub(r"_plddt.*$", "", name)
    name = re.sub(r"_coverage.*$", "", name)
    name = re.sub(r"\.a3m$", "", name)

    return name


