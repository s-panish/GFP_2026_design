def read_scores_json(path):
    with open(path, "r", encoding="utf-8") as handle:
        data = json.load(handle)

    plddt = data.get("plddt", None)
    ptm = data.get("ptm", np.nan)

    if plddt is None:
        raise ValueError(f"No plddt found in {path}")

    plddt = np.array(plddt, dtype=float)
    chromophore_indices = [pos - 1 for pos in CHROMOPHORE_POSITIONS]

    return {
        "Mean_pLDDT": float(np.mean(plddt)),
        "pTM": float(ptm) if ptm is not None else np.nan,
        "Chromophore_pLDDT_Mean_65_67": float(np.mean(plddt[chromophore_indices])),
        "Chromophore_pLDDT_Min_65_67": float(np.min(plddt[chromophore_indices])),
        "Core_pLDDT_Mean_1_220": float(np.mean(plddt[CORE_START - 1:CORE_END])),
        "CTerm_pLDDT_Mean_221_End": float(np.mean(plddt[220:])),
        "plddt_array": plddt
    }


def parse_mutation_positions(mutation_string):
    tokens = re.findall(r"[A-Z](\d+)[A-Z]", str(mutation_string))
    return [int(x) for x in tokens]


def read_ca_coordinates_from_pdb(path, start=1, end=220):
    coords = []
    residues = []

    with open(path, "r", encoding="utf-8", errors="ignore") as handle:
        for line in handle:
            if not line.startswith("ATOM"):
                continue

            atom_name = line[12:16].strip()
            if atom_name != "CA":
                continue

            try:
                resseq = int(line[22:26].strip())
                x = float(line[30:38])
                y = float(line[38:46])
                z = float(line[46:54])
            except ValueError:
                continue

            if start <= resseq <= end:
                residues.append(resseq)
                coords.append([x, y, z])

    return np.array(coords, dtype=float), residues


def kabsch_rmsd(P, Q):
    if P.shape != Q.shape:
        raise ValueError(f"Shape mismatch: {P.shape} vs {Q.shape}")

    P_cent = P - P.mean(axis=0)
    Q_cent = Q - Q.mean(axis=0)

    C = np.dot(P_cent.T, Q_cent)
    V, S, Wt = np.linalg.svd(C)

    d = np.sign(np.linalg.det(np.dot(V, Wt)))
    D = np.diag([1.0, 1.0, d])

    U = np.dot(np.dot(V, D), Wt)

    P_rot = np.dot(P_cent, U)
    diff = P_rot - Q_cent

    return float(np.sqrt(np.mean(np.sum(diff * diff, axis=1))))


def read_sequence_from_a3m(path):
    sequence_chunks = []
    reading_first_sequence = False
    first_header_seen = False

    with open(path, "r", encoding="utf-8", errors="ignore") as handle:
        for raw_line in handle:
            line = raw_line.strip()

            if line == "":
                continue

            if line.startswith(">"):
                if not first_header_seen:
                    first_header_seen = True
                    reading_first_sequence = True
                    continue
                else:
                    break

            if reading_first_sequence:
                sequence_chunks.append(line)

    sequence = "".join(sequence_chunks)
    sequence = re.sub(r"[a-z]", "", sequence)
    sequence = sequence.replace("-", "")
    sequence = normalize_sequence(sequence)

    if len(sequence) == 0:
        return None

    return sequence


THREE_TO_ONE = {
    "ALA": "A", "ARG": "R", "ASN": "N", "ASP": "D", "CYS": "C",
    "GLN": "Q", "GLU": "E", "GLY": "G", "HIS": "H", "ILE": "I",
    "LEU": "L", "LYS": "K", "MET": "M", "PHE": "F", "PRO": "P",
    "SER": "S", "THR": "T", "TRP": "W", "TYR": "Y", "VAL": "V"
}


def read_sequence_from_pdb(path):
    residues = []
    seen = set()

    with open(path, "r", encoding="utf-8", errors="ignore") as handle:
        for line in handle:
            if not line.startswith("ATOM"):
                continue

            atom_name = line[12:16].strip()
            if atom_name != "CA":
                continue

            resname = line[17:20].strip()
            chain_id = line[21].strip()
            resseq = line[22:26].strip()
            key = (chain_id, resseq)

            if key in seen:
                continue

            seen.add(key)
            residues.append(THREE_TO_ONE.get(resname, "X"))

    sequence = "".join(residues)

    if "X" in sequence:
        return None

    return sequence


def find_matching_key(query_id, key_collection):
    exact = [key for key in key_collection if key == query_id]
    if exact:
        return exact[0]

    contains = [key for key in key_collection if query_id in key]
    if contains:
        return contains[0]

    return None


